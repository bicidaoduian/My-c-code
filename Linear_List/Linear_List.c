#include <stdio.h>      /* 引入标准输入输出库，用于printf等函数 */
#include <stdlib.h>     /* 引入标准库，用于malloc动态内存分配和exit函数 */

/*------------------------------------------------------------------------------
 * 宏定义与类型声明部分
 *------------------------------------------------------------------------------*/

/* 
 * 【宏定义】#define Maxsize 100
 * 作用：定义线性表的最大容量为100
 * 说明：使用宏定义的好处是便于修改，只需改一处即可全局生效
 *       这里的100表示线性表最多存储100个元素
 */
#define Maxsize 100

/* 
 * 【类型定义】typedef int DataType;
 * 作用：将int类型重命名为DataType
 * 说明：这样做的好处是数据类型可扩展
 *       如果将来需要存储float、double或其他类型，
 *       只需将int改为对应类型即可，无需修改其他代码
 *       例如：typedef float DataType; 或 typedef char DataType;
 */
typedef int DataType;

/*------------------------------------------------------------------------------
 * 线性表结构体定义
 *------------------------------------------------------------------------------*/

/*
 * 【结构体定义】struct SeqList
 * 作用：定义线性表的存储结构
 * 成员说明：
 *   - data[Maxsize]: 数组，用于存储线性表中的元素
 *   - size: 整型变量，记录当前线性表中实际存储的元素个数
 */
struct SeqList
{
    DataType data[Maxsize];  /* 存储元素的数组，大小为Maxsize(100) */
    int size;                /* 当前元素个数，初始为0 */
};

/*
 * 【类型定义】typedef struct SeqList SeqList;
 * 作用：将struct SeqList简化为SeqList
 * 说明：之后可以直接使用 SeqList 来声明变量，无需每次都写 struct SeqList
 */
typedef struct SeqList List;

/*------------------------------------------------------------------------------
 * 函数声明部分
 *------------------------------------------------------------------------------*/

void ListInitiste(List *L);           /* 初始化线性表 */
int ListInsert(List *L, int i, DataType x);  /* 插入元素 */
int ListLength(List *L);              /* 求元素个数 */
int ListChange(List *L, int i, DataType x);  /* 修改/删除元素 */
int ListGet(List *L, int i, DataType *x);    /* 提取元素 */
void ListPrint(List *L);              /* 打印线性表（辅助函数） */

/*------------------------------------------------------------------------------
 * 函数实现部分
 *------------------------------------------------------------------------------*/

/*
 * 【初始化函数】void ListInitiste(SeqList *L)
 * 参数：L - 指向线性表的指针
 * 返回值：无
 * 功能：将线性表初始化为空表
 * 说明：只需将size设为0，表示表中没有任何元素
 *       此时虽然data数组存在，但其中的数据是无效的
 */
void ListInitiste(List *L)
{
    L->size = 0;  /* 将元素个数置为0，表示空表 */
    /* 
     * 注意：这里使用 L->size 而不是 L.size
     * 因为L是指针，需要用箭头运算符 -> 来访问成员
     * 等价于 (*L).size
     */
}

/*
 * 【插入函数】int ListInsert(SeqList *L, int i, DataType x)
 * 参数：L - 指向线性表的指针
 *       i - 插入位置（从1开始计数）
 *       x - 要插入的元素值
 * 返回值：成功返回1，失败返回0
 * 功能：在位置i处插入元素x，原位置及之后的元素依次后移
 * 说明：插入操作需要检查：
 *       1. 表是否已满（size >= Maxsize）
 *       2. 插入位置是否合法（1 <= i <= size+1）
 */
int ListInsert(List *L, int i, DataType x)
{
    int j;  /* 循环变量，用于元素后移 */
    
    /* 检查表是否已满 */
    if (L->size >= Maxsize)
    {
        printf("错误：线性表已满，无法插入！\n");
        return 0;  /* 插入失败，返回0 */
    }
    
    /* 检查插入位置是否合法 */
    /* i < 1 表示位置太小，i > L->size+1 表示位置太大 */
    if (i < 1 || i > L->size + 1)
    {
        printf("错误：插入位置不合法！\n");
        return 0;  /* 插入失败，返回0 */
    }
    
    /* 
     * 元素后移操作
     * 从最后一个元素开始，依次向后移动一位
     * 直到第i个位置空出来
     * 注意：数组下标从0开始，所以第i个位置的下标是i-1
     */
    for (j = L->size; j >= i; j--)  //设当前填充了0-9，此时j=10
    {
        L->data[j] = L->data[j - 1];  /* 元素后移一位 */
    }
    
    /* 在位置i处插入新元素x */
    L->data[i - 1] = x;  /* 第i个位置对应数组下标i-1 */
    
    /* 元素个数加1 */
    L->size++;
    
    return 1;  /* 插入成功，返回1 */
}

/*
 * 【求长度函数】int ListLength(SeqList *L)
 * 参数：L - 指向线性表的指针
 * 返回值：当前线性表中的元素个数
 * 功能：返回线性表中元素的个数
 * 说明：直接返回size成员即可，时间复杂度O(1)
 */
int ListLength(List *L)
{
    return L->size;  /* 返回当前元素个数 */
}

/*
 * 【修改/删除函数】int ListChange(SeqList *L, int i, DataType x)
 * 参数：L - 指向线性表的指针
 *       i - 要修改/删除的位置（从1开始计数）
 *       x - 新的元素值（当x为特殊值时表示删除）
 * 返回值：成功返回1，失败返回0
 * 功能：用x替代原表L的第i个位置元素
 *       当x为特定值（此处用-99999表示null）时，删除该位置元素
 * 说明：修改操作相对简单，直接替换即可
 *       删除操作需要将后续元素前移
 */
int ListChange(List *L, int i, DataType x)
{
    int j;  /* 循环变量 */
    
    /* 检查位置是否合法 */
    if (i < 1 || i > L->size)
    {
        printf("错误：位置不合法！\n");
        return 0;  /* 操作失败 */
    }
    
    /* 
     * 判断是否为删除操作
     * 这里使用-99999作为特殊标记表示"null"
     * 实际应用中可以用其他方式，如单独的删除函数或指针参数
     */
    if (x == -99999)  /* x为null，执行删除操作 */
    {
        /* 元素前移，覆盖要删除的元素 */
        for (j = i; j < L->size; j++)
        {
            L->data[j - 1] = L->data[j];    //设i=5，此时删除a[4](a[0]时i=1，则a[4]时i=5)
                                            //所以data[j-1]=data[4]，要替换成data[5]
        }
        L->size--;  /* 元素个数减1 */
        printf("位置 %d 的元素已删除\n", i);
    }
    else  /* 执行修改操作 */
    {
        L->data[i - 1] = x;  /* 直接替换第i个位置的元素 */
        printf("位置 %d 的元素已修改为 %d\n", i, x);
    }
    
    return 1;  /* 操作成功 */
}

/*
 * 【提取函数】int ListGet(SeqList *L, int i, DataType *x)
 * 参数：L - 指向线性表的指针
 *       i - 要提取的位置（从1开始计数）
 *       x - 指向存储提取元素的变量的指针
 * 返回值：成功返回1，失败返回0
 * 功能：取表里的第i个元素存于x中
 * 说明：使用指针参数x来返回提取的元素值
 *       这是一种常见的C语言参数传递方式
 */
int ListGet(List *L, int i, DataType *x)
{
    /* 检查位置是否合法 */
    if (i < 1 || i > L->size)
    {
        printf("错误：位置不合法！\n");
        return 0;  /* 提取失败 */
    }
    
    /* 将第i个元素存入x指向的变量中 */
    *x = L->data[i - 1];  /* 第i个位置对应数组下标i-1 */
    
    return 1;  /* 提取成功 */
}

/*
 * 【打印函数】void ListPrint(SeqList *L)
 * 参数：L - 指向线性表的指针
 * 返回值：无
 * 功能：打印线性表中的所有元素
 * 说明：这是一个辅助函数，用于测试和调试
 */
void ListPrint(List *L)
{
    int i;  /* 循环变量 */
    
    printf("线性表内容：[");
    for (i = 0; i < L->size; i++)
    {
        printf("%d", L->data[i]);
        if (i < L->size - 1)
        {
            printf(", ");  /* 元素之间用逗号分隔 */
        }
    }
    printf("]\n");
    printf("当前元素个数：%d\n", L->size);
}

/*------------------------------------------------------------------------------
 * 主函数
 *------------------------------------------------------------------------------*/

/*
 * 【主函数】int main()
 * 功能：测试线性表的各项功能
 *       将正整数1至10存于表L的前十个位置
 */
int main()
{
    List L;    /* 声明一个线性表变量L */
    int i;        /* 循环变量 */
    DataType x;   /* 用于存储提取的元素 */
    
    printf("========== 线性表操作演示 ==========\n\n");
    
    /* 1. 初始化线性表 */
    printf("【步骤1】初始化线性表\n");
    ListInitiste(&L);  /* 传递L的地址给函数 */
    printf("初始化完成，当前元素个数：%d\n\n", ListLength(&L));
    
    /* 2. 将正整数1至10存入表L的前十个位置 */
    printf("【步骤2】将正整数1至10存入表L的前十个位置\n");
    for (i = 1; i <= 10; i++)
    {
        /* 
         * ListInsert(&L, i, i) 的含义：
         * - &L: 线性表L的地址
         * - i: 插入位置（第i个位置）
         * - i: 插入的值（就是i本身，即1,2,3,...,10）
         */
        ListInsert(&L, i, i);
    }
    printf("插入完成！\n");
    ListPrint(&L);
    printf("\n");
    
    /* 3. 测试求长度函数 */
    printf("【步骤3】测试求长度函数\n");
    printf("当前线性表长度：%d\n\n", ListLength(&L));
    
    /* 4. 测试提取函数 */
    printf("【步骤4】测试提取函数\n");
    if (ListGet(&L, 5, &x))  /* 提取第5个元素 */
    {
        printf("第5个元素是：%d\n", x);
    }
    if (ListGet(&L, 1, &x))  /* 提取第1个元素 */
    {
        printf("第1个元素是：%d\n", x);
    }
    if (ListGet(&L, 10, &x))  /* 提取第10个元素 */
    {
        printf("第10个元素是：%d\n", x);
    }
    printf("\n");
    
    /* 5. 测试修改函数 */
    printf("【步骤5】测试修改函数\n");
    ListChange(&L, 3, 100);  /* 将第3个元素修改为100 */
    ListPrint(&L);
    printf("\n");
    
    /* 6. 测试删除函数（传入-99999表示删除） */
    printf("【步骤6】测试删除函数\n");
    ListChange(&L, 5, -99999);  /* 删除第5个元素 */
    ListPrint(&L);
    printf("\n");
    
    /* 7. 测试插入函数（在中间位置插入） */
    printf("【步骤7】测试在中间位置插入\n");
    ListInsert(&L, 3, 999);  /* 在第3个位置插入999 */
    ListPrint(&L);
    printf("\n");
    
    printf("========== 演示结束 ==========\n");
    
    return 0;  /* 程序正常结束 */
}
